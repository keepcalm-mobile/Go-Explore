const stateToProps = (state) => {
    return {
        items: state[ ModMap.Items ].items,
        data: state[ ModMap.Items ].bookingCinema,//tempData,
    };
};

const dispatchToProps = (dispatch) => {
    return {
        getCinemasData: (iValue) => dispatch(getCinemasData(iValue)),
        setScrollOffset: (iValue) => dispatch(setScrollOffset(iValue)),
    };
};

const mergeProps = (stateProps, dispatchProps, ownProps) => {
    const _itemId = ownProps.navigation.state.params.itemId;

    let sorted = [];
    if (stateProps.data.times) {
        for (let i = 0; i < stateProps.data.times.length; i++){
            let cinema = {value:stateProps.data.times[i].cinemaId, label:stateProps.data.times[i].cinemaName, times:[]};

            const maxDates = stateProps.data.dates.length > 7 ? 7 : stateProps.data.dates.length;
            for (let b = 0; b < maxDates; b++){
                cinema.times.push({...stateProps.data.dates[b], items:[]});
            }

            for (let ii = 0; ii < stateProps.data.times[i].showtimelist.length; ii++){
                const time = stateProps.data.times[i].showtimelist[ii];
                if (_itemId === time.movieId){
                    const index = cinema.times.findIndex(iItem => iItem.passingDate === time.showdate);
                    if (index !== -1){
                        cinema.times[index].items.push(time);
                    }
                }
            }
            sorted.push(cinema);
        }
    }
    //stateProps.items[_itemId].header
    return Object.assign({}, ownProps, {data: sorted, header: ownProps.navigation.state.params.tempHeader, ticketTypes:ticketTypes}, dispatchProps);
};


const Connected = connect(stateToProps, dispatchToProps, mergeProps, { forwardRef: true })(BookingTicket);

export default forwardRef((props, ref) =>
    <Connected {...props} ref={ref} />
);



